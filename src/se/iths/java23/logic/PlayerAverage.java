package se.iths.java23.logic;

public class PlayerAverage {
    private String name;
    private double average;
    public PlayerAverage(String name, double average) {
        this.name = name;
        this.average = average;
    }

    public String getName() {
        return name;
    }

    public double getAverage() {
        return average;
    }
}
